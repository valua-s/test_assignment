class CourseViewSet(viewsets.ModelViewSet):
    """Курсы """

    queryset = Course.objects.all()
    permission_classes = (ReadOnlyOrIsAdmin,)

    def get_serializer_class(self):
        if self.action in ['list', 'retrieve']:
            return CourseSerializer
        return CreateCourseSerializer

    @action(
        methods=['post'],
        detail=True,
        permission_classes=(permissions.IsAuthenticated,)
    )
    def pay(self, request, pk):
        """Покупка доступа к курсу (подписка на курс)."""
        user = request.user
        serializer = SubscriptionSerializer(student=user.id,
                                            cource=pk)
        if serializer.is_valid(raise_exception=True):
            serializer.save()
            return Response(serializer.validated_data,
                            status=status.HTTP_201_CREATED)

    @action(
        methods=['get'],
        detail=False,
        permission_classes=(permissions.IsAuthenticated,))
    def get_cources_for_user(self, request):
        user = request.user
        purchased_courses = Subscription.objects.filter(
            student=user).values_list('course_id', flat=True)
        query_set = Course.objects.exclude(id__in=purchased_courses)
        serializer = CourseSerializer(query_set, many=True)
        return Response(data=serializer.data,
                        status=status.HTTP_200_OK)
